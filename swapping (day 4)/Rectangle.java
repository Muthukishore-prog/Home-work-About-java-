class Rectangle
{
    public static void main(String[] args)
	{
        float length = 10.10f;
        float breadth = 5.50f;

        float area = length * breadth;
        float perimeter = 2 * (length + breadth);

        System.out.println("Area = " + area);
        System.out.println("Perimeter = " + perimeter);
    }
}